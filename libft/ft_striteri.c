#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	if (s == NULL || f == NULL)
		return ;
	i = -1;
	while (*s != '\0')
	{
		f((unsigned char)(++i), s);
		s++;
	}
}
